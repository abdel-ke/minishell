#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*temp;

	if (!lst || !f)
		return ;
	temp = lst;
	if (temp != NULL && f)
	{
		while (temp->next != NULL)
		{
			f(temp->content);
			temp = temp->next;
		}
	}
}
